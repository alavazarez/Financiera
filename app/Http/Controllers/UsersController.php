    public function edit()
    {
        $usuario = User::find(Auth::User()->id);

        return view('EditUser')->with('usuario', $usuario);
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::findOrFail($id);

        $user->name = $request->name;
        $user->email = $request->email;
        $user->save();

        return redirect()->route('home');
    }

    public function password(Request $request, $id)
    {
        $user = User::findOrFail($id);
        if( !Hash::check($request->passwordActual, $user->password))
        {
            return redirect()->route('home')->with('flash', 'La contraseña actual no es correcta');
        }
        else if($request->passwordNew == $request->passwordConfirm)
        {   
            $user->password = Hash::make($request->passwordNew);
            $user->save();
            return redirect()->route('home');
        }
        return redirect()->route('home')->with('flash', 'La nueva contraseña no coincide con la confirmacion');
    }

